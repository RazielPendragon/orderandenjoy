CREATE TABLE restaurantes.usuario_comensal (
	usuario_comensal_id INT auto_increment NOT NULL,
	usuario_comensal_correo varchar(100) NOT NULL,
	usuario_comensal_nombre_completo varchar(300) NOT NULL,
	usuario_comensal_nombre_telefono int(10) NOT NULL,
	CONSTRAINT usuario_comensal_PK PRIMARY KEY (usuario_comensal_id)
)








































